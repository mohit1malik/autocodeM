package me.learning.learn;

public class Ecapsulation {
    
    private int a , b;

    public void setA(int x){
        this.a = x;
    }
    
    public void setB(int y){
        this.b = y;
    }
     

    public int getA(){
        return a;
    }

    public int getB(){
        return b;
    }
    
}


